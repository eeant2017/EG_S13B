#include "UserRTC.h"



//******************************************* 外部变量声明 *********************************************//
extern uint32 cur_time;


//********************************************************************************************************
// Function name	: SetSysRTCTime
// Description		: 设置系统RTC时间
// Parameter		: *arr	: 
//					: size	
// Return			: none
//--------------------------------------------------------------------------------------------------------
// Info				: 
//********************************************************************************************************
void SetSysRTCTime(uint8_t *arr)
{
	uint8_t *p = arr;
	cur_time = BYTE2LONG(*p, *(p + 1), *(p + 2), *(p + 3));
	RTC_SetUnixTime(cur_time);
}

